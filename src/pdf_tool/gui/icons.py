"""GUI 아이콘 매핑 모듈.

9개 기능별 유니코드 아이콘과 모드별 색상을 정의한다.
"""

from __future__ import annotations

# ============================================================================
# 기능별 유니코드 아이콘 매핑
# ============================================================================

ICON_MAP: dict[str, str] = {
    "Cut": "\u2702",           # 가위
    "Merge": "\u2795",         # 플러스
    "Split": "\u2702",         # 가위 (분할)
    "Rotate": "\u21BB",        # 시계 방향 회전 화살표
    "Resize": "\u2922",        # 대각선 화살표
    "Compress": "\u2B73",      # 아래로 압축
    "Watermark": "\U0001F4A7",  # 물방울
    "Images to PDF": "\U0001F5BC",  # 프레임 이미지
    "Info": "\u2139",          # 정보 기호
}


# ============================================================================
# 다크/라이트 모드별 아이콘 색상
# ============================================================================

ICON_COLORS: dict[str, str] = {
    "dark": "#FFFFFF",
    "light": "#000000",
}


# ============================================================================
# 유틸리티 함수
# ============================================================================


def get_icon(name: str, *, default: str = "") -> str:
    """기능 이름에 해당하는 아이콘을 반환한다.

    Args:
        name: 기능 이름 (예: "Cut", "Merge")
        default: 아이콘이 없을 때 반환할 기본값

    Returns:
        유니코드 아이콘 문자열
    """
    return ICON_MAP.get(name, default)
